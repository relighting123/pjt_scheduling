-- @db: Prd
-- 1회성 마이그레이션: CRT_TM/CHG_TM DATE/TIMESTAMP → VARCHAR2(19) 'YYYY-MM-DD HH24:MI:SS'
--
-- rts_output_tables.sql의 DDL만 바꿔서는 이미 생성돼 있는 실제 테이블에는 반영되지
-- 않는다. CRT_TM/CHG_TM 컬럼이 아직 DATE/TIMESTAMP인 상태에서 새 코드가 문자열
-- (TO_CHAR(SYSDATE, 'YYYY-MM-DD HH24:MI:SS'))을 넣으려 하면 세션 NLS_DATE_FORMAT
-- 기준으로 암묵 변환을 시도하다가 ORA-01843(not a valid month)이 발생한다.
-- 이 스크립트로 실제 컬럼 타입을 VARCHAR2(19)로 바꿔야 한다.
--
-- 이미 VARCHAR2로 마이그레이션된 테이블에 다시 실행하면 ALTER ... ADD에서
-- ORA-00957(중복 컬럼 이름) 등으로 실패한다 — 테이블별로 컬럼 타입을 확인 후
-- 필요한 부분만 실행할 것. (전체를 한 번에 실행하도록 만들어진 스크립트가 아님)

-- RTS_RSLT_INF
ALTER TABLE RTS_RSLT_INF ADD (CRT_TM_NEW VARCHAR2(19));
UPDATE RTS_RSLT_INF SET CRT_TM_NEW = TO_CHAR(CRT_TM, 'YYYY-MM-DD HH24:MI:SS');
ALTER TABLE RTS_RSLT_INF MODIFY (CRT_TM_NEW NOT NULL);
ALTER TABLE RTS_RSLT_INF DROP COLUMN CRT_TM;
ALTER TABLE RTS_RSLT_INF RENAME COLUMN CRT_TM_NEW TO CRT_TM;

